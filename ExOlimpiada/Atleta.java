package ExOlimpiada;

public class Atleta {
    private String nome;
    private String pais;
    private float dist;

    public Atleta(String nome, String pais, Float dist) {

    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public float getDist() {
        return dist;
    }

    public void setDist(float dist) {
        this.dist = dist;
    }
}
